	private void tbthietbiMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tbthietbiMouseClicked
		// TODO add your handling code here:

		ProcessCrt(true);
		this.BT_ghilai.setEnabled(true);
		try {
			int row = this.tbthietbi.getSelectedRow();
			String IDrow = (this.tbthietbi.getModel().getValueAt(row, 0)).toString();
			String sql = "SELECT * FROM thietbi where matb='" + IDrow + "'";
			ResultSet rs = UpdateTable.ShowTextField(sql);
			if (rs.next()) {
				matb = rs.getString("matb");
				this.txtmatb.setText(rs.getString("matb"));
				this.txttentb.setText(rs.getString("tentb"));

				this.txttskt.setText(rs.getString("tskt"));
				this.txtgia.setText(rs.getString("gia"));
				this.txtnsx.setText(rs.getString("nsx"));
				this.txtthoigiancap.setDate(rs.getDate("thoigiancap"));

			}
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, e);
		}

	}// GEN-LAST:event_tbthietbiMouseClicked

