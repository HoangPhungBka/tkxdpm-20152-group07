	private void BT_ghilaiActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BT_ghilaiActionPerformed
		// TODO add your handling code here:
		if (this.txtmatb.getText().length() == 0)
			JOptionPane.showMessageDialog(null, "Bạn vui lòng chọn dòng muốn sửa", "thông báo", 2);

		else if (this.txttentb.getText().length() == 0)
			JOptionPane.showMessageDialog(null, "Tên thiết bị không thể bỏ trống", "thông báo", 2);
		else if (this.txttskt.getText().length() == 0)
			JOptionPane.showMessageDialog(null, "Thông số ký thuật không thể bỏ trống", "thông báo", 2);
		else if (this.txtgia.getText().length() == 0)
			JOptionPane.showMessageDialog(null, "Giá không thể bỏ trống", "thông báo", 2);
		else if (this.txtnsx.getText().length() == 0)
			JOptionPane.showMessageDialog(null, "Nhà sản xuất không thể bỏ trống", "thông báo", 2);

		else {

			ThietBi tb;
			tb = new ThietBi(this.txtmatb.getText(), this.txttentb.getText(), cbmadv.getSelectedItem().toString(), this.txttskt.getText(),
					Double.parseDouble(this.txtgia.getText()), Integer.parseInt(cbtrangthai.getSelectedItem().toString()), this.txtnsx.getText(),
					this.txtthoigiancap.getDate());
			ThietBiController.SuaThietBi(tb);
			showCT();
			ProcessCrt(true);

			if (ThietBiController.SuaThietBi(tb)) {

				JOptionPane.showMessageDialog(null, "Bạn đã sửa thành công", "Thông báo", 1);
			}

		}

	}// GEN-LAST:event_BT_ghilaiActionPerformed

