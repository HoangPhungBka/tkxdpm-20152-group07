	@SuppressWarnings("unchecked")
	public void DataComboBox() {
		String sql1 = " SELECT * FROM newschema.donvi";
		try {
			PreparedStatement pst = conn.prepareStatement(sql1);
			ResultSet rs = pst.executeQuery();
			while (rs.next()) {
				this.cbmadv.addItem(rs.getString("madv"));
				this.cbdv.addItem(rs.getString("madv"));

			}
		} catch (SQLException | HeadlessException e) {
			JOptionPane.showMessageDialog(null, e);
		}

		String sql2 = " SELECT DISTINCT nsx FROM newschema.thietbi";
		try {
			PreparedStatement pst = conn.prepareStatement(sql2);
			ResultSet rs = pst.executeQuery();
			while (rs.next()) {
				this.cbcungcap.addItem(rs.getString("nsx"));

			}
		} catch (SQLException | HeadlessException e) {
			JOptionPane.showMessageDialog(null, e);
		}
		String sql3 = " SELECT DISTINCT trangthai FROM newschema.thietbi";
		try {
			PreparedStatement pst = conn.prepareStatement(sql3);
			ResultSet rs = pst.executeQuery();
			while (rs.next()) {
				this.cbtinhtrang.addItem(rs.getString("trangthai"));

			}
		} catch (SQLException | HeadlessException e) {
			JOptionPane.showMessageDialog(null, e);
		}

	}

